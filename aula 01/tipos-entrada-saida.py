nome = 'Marcos'  # variavel nome
idade = 15  # variavel idade
altura = 1.72  # variavel altura

tipo_nome = type(nome)  # tipo da variavel nome
tipo_idade = type(idade)  # tipo da variavel idade
tipo_altura = type(altura)  # tipo da variavel altura

print(tipo_nome)  # imprimir tipo da variavel
print(tipo_idade)  # imprimir tipo da variavel
print(tipo_altura)  # imprimir tipo da variavel

print(type(nome), type(idade), type(altura))  # juntando tudo em uma unica linha
print(nome, 'tem', idade, 'anos e',
      altura, 'de altura')
